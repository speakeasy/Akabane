package org.bibanon.akabane.users;

/**
 *
 * @author speakeasy
 */
class User {

    private final String name;
    private final Rank rank;

    public User(String name, Rank rank) {
        this.name = name;
        this.rank = rank;
    }

    public String getName() {
        return name;
    }

    public Rank getRank() {
        return rank;
    }
}
